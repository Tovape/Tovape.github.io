$(function() {

	if(!localStorage.getItem("language")){
		localStorage.setItem("language", "es");
	}

	var lang = localStorage.getItem("language");
	
	$('p,a,h1,h2,ul').each(function(index, element) {
		$(this).html(arrLang[lang][$(this).attr('key')]);
	});

	$('.language-each').click(function() {
		$(this).addClass('active').siblings().removeClass('active');
		localStorage.setItem("language", $(this).attr('id'));
		var lang = $(this).attr('id');

		$('p,a,h1,h2,ul').each(function(index, element) {
			$(this).html(arrLang[lang][$(this).attr('key')]);
		});

	});

});

$(document).ready(function() {
	
	const languagebutton = document.querySelectorAll(".footer-language p");
	var language = localStorage.getItem("language");

	if (language == 'ca') {
		languagebutton[0].classList.add('active');
	} else if (language == 'es') {
		languagebutton[1].classList.add('active');
	} else if (language == 'en') {
		languagebutton[2].classList.add('active');
	}

});